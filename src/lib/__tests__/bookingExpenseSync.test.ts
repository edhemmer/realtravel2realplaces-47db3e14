// v2.2.1: TRIP TOTAL VERIFICATION TESTS
// These tests verify that totals use expense records, not raw segments
// ============================================================

describe('airfare expense creation logic', () => {
  it('creates exactly one expense for a multi-leg booking', () => {
    // This tests the conceptual flow - syncExpenseFromBooking creates ONE expense
    // per booking, using getBookingExpenseCost for the amount
    const multiLegBooking = mockBooking({
      id: 'booking-roundtrip-1',
      booking_type: 'flight',
      airline: 'Delta',
      total_cost: 500,
      notes: 'Outbound: DL100, DL200. Return: DL300, DL400', // 4 segments
    });
    
    // The cost that would be used for the expense
    const expenseCost = getBookingExpenseCost(multiLegBooking);
    
    // Should be exactly 500, not 500*4 or 500*2
    expect(expenseCost).toBe(500);
  });

  it('prevents double counting by using booking-level total only', () => {
    // Simulate a scenario where we might accidentally double count
    const outboundLeg = mockBooking({
      id: 'leg-1',
      total_cost: 500, // Same total on each "leg" would be wrong
    });
    const returnLeg = mockBooking({
      id: 'leg-2',
      total_cost: 500,
    });
    
    // In correct implementation, these should be ONE booking with total_cost: 500
    // Not TWO bookings with total_cost: 500 each
    // The parsing rules ensure this, but we verify the cost function handles it
    expect(getBookingExpenseCost(outboundLeg)).toBe(500);
    expect(getBookingExpenseCost(returnLeg)).toBe(500);
    
    // Total if incorrectly stored as separate bookings: 1000 (WRONG)
    // Total if correctly stored as single booking: 500 (CORRECT)
    // The fix ensures parsing creates ONE booking with the total
  });
});