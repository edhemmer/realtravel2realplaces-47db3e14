describe('getBookingExpenseCost', () => {
  describe('multi-leg booking with single total fare', () => {
    it('returns booking total_cost as the expense amount', () => {
      // Scenario: Round-trip flight with 2 segments, single total of $500
      const booking = mockBooking({
        booking_type: 'flight',
        total_cost: 500,
        // Note: In real data, segment info would be in notes or separate records
        // but the booking.total_cost is the single source of truth
      });
      
      expect(getBookingExpenseCost(booking)).toBe(500);
    });

    it('uses booking total for multi-segment flights', () => {
      // Scenario: 3-leg flight (e.g., A->B->C and back) with single total
      const booking = mockBooking({
        booking_type: 'flight',
        airline: 'United',
        total_cost: 750,
        notes: 'Outbound: UA123, UA456. Return: UA789',
      });
      
      // Only the booking-level total is used, not multiplied by legs
      expect(getBookingExpenseCost(booking)).toBe(750);
    });
  });

  describe('booking without total (segment prices only scenario)', () => {
    it('returns 0 when total_cost is null', () => {
      const booking = mockBooking({
        booking_type: 'flight',
        total_cost: null as unknown as number,
      });
      
      // No expense should be created - returns 0
      expect(getBookingExpenseCost(booking)).toBe(0);
    });

    it('returns 0 when total_cost is undefined', () => {
      const booking = mockBooking({
        booking_type: 'flight',
        total_cost: undefined as unknown as number,
      });
      
      expect(getBookingExpenseCost(booking)).toBe(0);
    });

    it('returns 0 when total_cost is 0', () => {
      const booking = mockBooking({
        booking_type: 'flight',
        total_cost: 0,
      });
      
      expect(getBookingExpenseCost(booking)).toBe(0);
    });
  });

  describe('booking with both total and segment amounts present', () => {
    it('uses only booking total, ignores segment data', () => {
      // Scenario: Booking has total_cost = 400
      // Segments might have individual amounts but we ignore them
      const booking = mockBooking({
        booking_type: 'flight',
        total_cost: 400,
        notes: 'Leg 1: $200, Leg 2: $200', // This is just informational
      });
      
      // Only booking.total_cost is used
      expect(getBookingExpenseCost(booking)).toBe(400);
    });
  });

  describe('edge cases for fare normalization', () => {
    it('handles negative cost (treats as invalid)', () => {
      const booking = mockBooking({
        total_cost: -100,
      });
      
      expect(getBookingExpenseCost(booking)).toBe(0);
    });

    it('handles NaN cost', () => {
      const booking = mockBooking({
        total_cost: NaN,
      });
      
      expect(getBookingExpenseCost(booking)).toBe(0);
    });

    it('handles Infinity cost', () => {
      const booking = mockBooking({
        total_cost: Infinity,
      });
      
      expect(getBookingExpenseCost(booking)).toBe(0);
    });

    it('handles string coerced to number', () => {
      const booking = mockBooking({
        total_cost: '350.50' as unknown as number,
      });
      
      // Number() coercion should work
      expect(getBookingExpenseCost(booking)).toBe(350.50);
    });

    it('handles decimal values correctly', () => {
      const booking = mockBooking({
        total_cost: 299.99,
      });
      
      expect(getBookingExpenseCost(booking)).toBe(299.99);
    });
  });

  describe('non-flight bookings', () => {
    it('uses total_cost for stay bookings', () => {
      const booking = mockBooking({
        booking_type: 'stay',
        property_name: 'Grand Hotel',
        total_cost: 450,
      });
      
      expect(getBookingExpenseCost(booking)).toBe(450);
    });

    it('uses total_cost for car rental bookings', () => {
      const booking = mockBooking({
        booking_type: 'car_rental',
        rental_company: 'Hertz',
        total_cost: 275,
      });
      
      expect(getBookingExpenseCost(booking)).toBe(275);
    });

    it('uses total_cost for activity bookings', () => {
      const booking = mockBooking({
        booking_type: 'activity',
        vendor_name: 'Theme Park',
        total_cost: 120,
      });
      
      expect(getBookingExpenseCost(booking)).toBe(120);
    });
  });
});

