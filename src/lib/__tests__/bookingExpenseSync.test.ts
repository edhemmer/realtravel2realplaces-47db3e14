 describe('extractBookingIdFromNotes', () => {
   it('extracts booking ID from notes', () => {
     const id = extractBookingIdFromNotes('[linked_booking:abc-123]');
     expect(id).toBe('abc-123');
   });
 
   it('extracts ID when marker is embedded in text', () => {
     const id = extractBookingIdFromNotes('Flight booking [linked_booking:abc-123] some notes');
     expect(id).toBe('abc-123');
   });
 
   it('returns null for notes without marker', () => {
     expect(extractBookingIdFromNotes('Regular notes')).toBeNull();
   });
 
   it('returns null for null/undefined notes', () => {
     expect(extractBookingIdFromNotes(null)).toBeNull();
     expect(extractBookingIdFromNotes(undefined)).toBeNull();
   });
 
   it('returns null for incomplete marker', () => {
     expect(extractBookingIdFromNotes('[linked_booking:abc-123')).toBeNull();
     expect(extractBookingIdFromNotes('linked_booking:abc-123]')).toBeNull();
   });
 });
 
