export function getVendorUrl(name: string, type: 'flight' | 'stay' | 'car_rental' | 'activity' | 'transport'): string | null {
  const normalizedName = name.toLowerCase().trim();
  
  if (type === 'flight') {
    // Check both name and partial matches
    for (const [key, url] of Object.entries(AIRLINE_URLS)) {
      if (normalizedName.includes(key) || key.includes(normalizedName)) {
        return url;
      }
    }
  }
  
  if (type === 'stay') {
    for (const [key, url] of Object.entries(HOTEL_URLS)) {
      if (normalizedName.includes(key) || key.includes(normalizedName)) {
        return url;
      }
    }
  }
  
  if (type === 'car_rental') {
    for (const [key, url] of Object.entries(CAR_RENTAL_URLS)) {
      if (normalizedName.includes(key) || key.includes(normalizedName)) {
        return url;
      }
    }
  }
  
  // Transport type - no auto-fill for now
  return null;
}

// Get all matches for autocomplete suggestions
export function getVendorSuggestions(query: string, type: 'flight' | 'stay' | 'car_rental' | 'activity' | 'transport'): string[] {
  if (!query || query.length < 2) return [];
  
  const normalizedQuery = query.toLowerCase().trim();
  const urls = type === 'flight' ? AIRLINE_URLS : 
               type === 'stay' ? HOTEL_URLS : 
               type === 'car_rental' ? CAR_RENTAL_URLS : {};
  
  const matches = Object.keys(urls)
    .filter(key => key.includes(normalizedQuery))
    .map(key => key.split(' ').map(word => word.charAt(0).toUpperCase() + word.slice(1)).join(' '))
    .slice(0, 5);
  
  // Remove duplicates
  return [...new Set(matches)];
}
